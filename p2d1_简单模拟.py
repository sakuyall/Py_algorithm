"""
第二部分:用数字模拟低洼地
    简化为上坡与下坡问题，当下坡被上坡终止，则生成一个低洼地
下一值小于本值则为下坡，大于则为上坡
题目提供了数据:3 6 1 4 8 3 2 5
"""
# 若按照已给输入写法则是老两句
list = [3,6,1,4,8,3,2,5]
n = len(list)
ans = 0         # 低洼地数量
mark = 0
# 设定0为上升，1为下降
for i in range(1, n):
    if list[i] < list[i - 1]:
        mark = 1
    elif list[i] > list[i - 1] and mark == 1:
        # 双层判断，下次为上升且前一次为下降
        mark = 0
        ans += 1

print(ans)